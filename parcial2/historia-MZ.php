    <p>
        Desde su creación en 1943 por el empresario sinaloense Alfonso Zaragoza Maytorena, 
        los supermercados MZ funcionan buscando dar a su clientela el mejor servicio y la mejor 
        calidad de productos para que su experiencia de compra sea inolvidable. 

        En los años 70 supermercados MZ siguió creciendo por todo el estado de Sinaloa y sería a 
        partir del año 2000 en los que se consolidaría como el líder de los supermercados 
        independientes en Sinaloa con 17 unidades por todo el estado.
    </p>
    <img src="img/historia1.jpg" alt="historia1"><br>
    <p>
        Así mismo el grupo MZ ha seguido incursionando en otros negocios como son las mueblerías 
Zaragoza y los almacenes para construcción Construzeta siguiendo la filosofía de conseguir 
los mejores productos para el cliente a los mejores precios.
<br>

Aunque supermercados MZ es una marca regional su concentración en el estado de Sinaloa los 
hace el grupo de supermercados independientes más grande de Sinaloa, existen 45 supermercados 
MZ por todo Sinaloa, de estos 20 están situados en la ciudad de Culiacán y el resto de 
supermercados MZ están repartidos en las ciudades de El Fuerte, Choix, Ahome, Guasave, 
Guamúchil, Angostura, Mocorito, Navolato, Guasave, La Cruz y El Rosario.
    </p>
    <p>
        La gama de productos de los supermercados MZ siempre es de primera calidad, así la carne que 
vende supermercados MZ proviene de su filial MZ Ganadera que se dedica al engorde y empaque 
de la carne que después se venderá en los supermercados MZ fresca, de primera calidad y sin 
ningún tipo de hormonas o clembuterol.

Ofertas y Promociones supermercados MZ.

Las ofertas de supermercados MZ siempre traen las mejores promociones cada semana para que cada 
peso rinda mucho más a la hora de hacer la despensa y siempre con la misma calidad y frescura 
que supermercados MZ garantiza.

Así mismo las promociones de supermercados MZ para ocasiones especiales como Navidades, Cuaresma, 
Reyes y por supuesto para las Fiestas Patrias propician que aún se puedan disfrutar más con la familia 
sin acabar con el presupuesto.
    </p>
